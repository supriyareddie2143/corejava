package com.tnsif.stringdemo;

public class Comparedemo {
	public static void main(String[] args) {
		String s1="sachin";
		String s2="rahul";
		String s3="rohan";
		System.out.println(s1.compareTo(s2));
		System.out.println(s2.compareTo(s3));
		System.out.println(s3.compareTo(s1));
	}

}
